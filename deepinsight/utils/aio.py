import asyncio


def get_or_create_loop():
    """
    Get the current event loop if one exists, or create a new one if none exists.

    This function attempts to get the currently running event loop. If no loop is set
    (raises RuntimeError), it creates a new event loop and sets it as the current loop.

    Returns:
        asyncio.AbstractEventLoop: The existing or newly created event loop.
    """
    try:
        loop = asyncio.get_event_loop()
    except RuntimeError:
        loop = asyncio.new_event_loop()
        asyncio.set_event_loop(loop)
    return loop
